public class Category {

    private String nameFoodCategoy;
    private String imageFoodCategory;
    private String idCategory;

    public String getNameFoodCategoy() {
        return nameFoodCategoy;
    }

    public void setNameFoodCategoy(String nameFoodCategoy) {
        this.nameFoodCategoy = nameFoodCategoy;
    }

    public String getImageFoodCategory() {
        return imageFoodCategory;
    }

    public void setImageFoodCategory(String imageFoodCategory) {
        this.imageFoodCategory = imageFoodCategory;
    }

    public String getIdCategory() {
        return idCategory;
    }

    public void setIdCategory(String idCategory) {
        this.idCategory = idCategory;
    }
}
